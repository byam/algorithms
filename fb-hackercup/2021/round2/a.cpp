void solve() {
    // in
    int n, m;
    read(n, m);
    vi s(m);
    read(s);

    used.clear();
    used.resize(m, false);

    ll ans = 0;
    vvi p(n, vi(m));
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            int x;
            read(x);
            p[i][j] = x;
        }
    }

    for (auto x : p) {
        calculate(s, x, ans);
        s = x;
    }

    out(ans);
}

