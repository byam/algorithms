void solve() {
    // in
    int n;
    string t, b;
    read(n, t, b);

    ll ans = 0;
    bool one = false;
    bool zero = false;
    for (int i = 0; i < n; i++) {
        // 01
        if (t[i] != b[i]) {
            ans += 2;
            one = false;
            zero = false;
            continue;
        }

        // 11:
        if (t[i] == '1') {
            // prev 0
            if (zero) {
                ans += 1;
                zero = false;
                one = false;
            }
            // remind one
            else {
                zero = false;
                one = true;
            }
            continue;
        }

        // 00:
        if (t[i] == '0') {
            // prev 1
            if (one) {
                ans += 2;
                zero = false;
                one = false;
            }
            // remind zero
            else {
                ans += 1;
                zero = true;
                one = false;
            }
            continue;
        }
    }

    out(ans);
}

int main() {
    // make input and output more efficient
    ios::sync_with_stdio(0);
    cin.tie(0);

    int t;
    t = 1;
    cin >> t;
    while (t--) solve();
    return 0;
}
